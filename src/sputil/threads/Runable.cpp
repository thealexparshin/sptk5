#include <sptk5/threads/Runable.h>

using namespace std;
using namespace sptk;

Runable::Runable() 
: m_terminated(false)
{
}

void Runable::execute()
{
    m_terminated = false;
    lock_guard<mutex> lock(m_running);
    run();
}

void Runable::terminate()
{
    m_terminated.store(true);
}

bool Runable::terminated()
{
    return m_terminated.load();
}
